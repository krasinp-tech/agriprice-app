SELECT DISTINCT product_name, 'ผลไม้'
FROM seed_thai_fruit_catalog
ON CONFLICT (product_name) DO UPDATE
SET category = EXCLUDED.category;

INSERT INTO public.varieties (product_id, variety_name)
SELECT p.product_id, seed.variety_name
FROM seed_thai_fruit_catalog seed
JOIN public.products p ON p.product_name = seed.product_name
ON CONFLICT (product_id, variety_name) DO NOTHING;

