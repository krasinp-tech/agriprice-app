INSERT INTO public.varieties (product_id, variety_name)
SELECT products.product_id, seed.variety_name
FROM seed_thai_agricultural_catalog AS seed
JOIN public.products ON products.product_name = seed.product_name
ON CONFLICT (product_id, variety_name) DO NOTHING;

