function getCurrentLang() {
  return localStorage.getItem('lang') || 'th';
}

function setCurrentLang(lang) {
  localStorage.setItem('lang', lang);
  // Trigger storage event manually for current tab
  window.dispatchEvent(new StorageEvent('storage', { key: 'lang', newValue: lang }));
}

function translatePage() {
  const lang = getCurrentLang();
  const t = TRANSLATIONS[lang] || TRANSLATIONS.th;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.setAttribute('placeholder', t[key]);
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (t[key]) el.setAttribute('title', t[key]);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria-label');
    if (t[key]) el.setAttribute('aria-label', t[key]);
  });

  window.dispatchEvent(new CustomEvent('i18n:updated', { detail: { lang } }));
}

function i18nT(key, fallback) {
  const lang = getCurrentLang();
  const t = TRANSLATIONS[lang] || TRANSLATIONS.th;
  if (Object.prototype.hasOwnProperty.call(t, key)) return t[key];
  const th = TRANSLATIONS.th || {};
  if (Object.prototype.hasOwnProperty.call(th, key)) return th[key];
  return fallback == null ? key : fallback;
}

function setupLanguageSelector() {
  const selector = document.getElementById('langSelector');
  if (!selector) return;
  selector.innerHTML = '';
  const wrap = document.createElement('div');
  wrap.className = 'lang-selector-wrap';
  const current = getCurrentLang();
  LANGUAGES.forEach(lang => {
    const btn = document.createElement('button');
    btn.type = 'button';
    btn.className = 'lang-btn' + (current === lang ? ' active' : '');
    btn.textContent = LANGUAGE_LABELS[lang] || lang.toUpperCase();
    btn.onclick = () => {
      setCurrentLang(lang);
      translatePage();
      setupLanguageSelector(); // update active state
    };
    wrap.appendChild(btn);
  });
  selector.appendChild(wrap);
}

window.i18nInit = function() {
  setupLanguageSelector();
  translatePage();
};

window.i18nT = i18nT;

