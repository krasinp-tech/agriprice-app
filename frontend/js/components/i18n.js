  const TRANSLATED_ATTRS = new Set([
    'data-i18n-placeholder',
    'data-i18n-title',
    'data-i18n-aria-label',
    'data-i18n-alt',
    'data-i18n-value'
  ]);

  function getI18nParams(el) {
    const params = {};
    if (!el || !el.attributes) return params;

    Array.from(el.attributes).forEach(attr => {
      if (!attr.name.startsWith('data-i18n-')) return;
      if (TRANSLATED_ATTRS.has(attr.name)) return;

      const rawName = attr.name.slice('data-i18n-'.length);
      if (!rawName) return;
      params[rawName] = attr.value;
      params[rawName.replace(/-/g, '_')] = attr.value;
      params[rawName.replace(/-([a-z])/g, (_, ch) => ch.toUpperCase())] = attr.value;
    });

    return params;
  }

  function interpolate(value, params) {
    const text = String(value == null ? '' : value);
    if (!params || typeof params !== 'object') return text;
    return text.replace(/\{([A-Za-z0-9_-]+)\}/g, (match, key) => {
      return Object.prototype.hasOwnProperty.call(params, key) ? params[key] : match;
    });
  }

  function escapeHtml(value) {
    return String(value)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#39;');
  }

  let legacyByteMap = null;
  let mojibakeObserverStarted = false;
  let mojibakeRepairQueued = false;
  let mojibakeRepairing = false;
  function getLegacyByteMap() {
    if (legacyByteMap) return legacyByteMap;
    legacyByteMap = new Map();

    try {
      const decoder = new TextDecoder('windows-874');
      for (let i = 0; i <= 255; i++) {
        const char = decoder.decode(new Uint8Array([i]));
        if (char && char !== '\uFFFD' && char.length === 1 && !legacyByteMap.has(char)) {
          legacyByteMap.set(char, i);
        }
      }
    } catch (_) {
      legacyByteMap = null;
    }

    return legacyByteMap;
  }

  function looksMojibake(value) {
    return /เธ|เน[\u0080-\u009F\u20AC\u2018-\u201D]|โ[\u0080-\u009F\u20AC\u2018-\u201D]/.test(String(value || ''));
  }

  function decodeMojibake(value) {
    const text = String(value == null ? '' : value);
    if (!looksMojibake(text)) return text;

    const map = getLegacyByteMap();
    if (!map) return text;

    const bytes = [];
    for (const char of text) {
      const code = char.charCodeAt(0);
      if (code <= 0x7F) {
        bytes.push(code);
      } else if (map.has(char)) {
        bytes.push(map.get(char));
      } else {
        return text;
      }
    }

    try {
      const decoded = new TextDecoder('utf-8', { fatal: true }).decode(new Uint8Array(bytes));
      if (!decoded || decoded.includes('\uFFFD') || !/[\u0E00-\u0E7F]/.test(decoded)) return text;
      return decoded;
    } catch (_) {
      return text;
    }
  }

  function repairMojibakeDom() {
    if (!document.body) return;
    if (mojibakeRepairing) return;
    mojibakeRepairing = true;

    try {
      const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
      const textNodes = [];
      while (walker.nextNode()) textNodes.push(walker.currentNode);
      textNodes.forEach(node => {
        const fixed = decodeMojibake(node.nodeValue);
        if (fixed !== node.nodeValue) node.nodeValue = fixed;
      });

      const attrs = ['placeholder', 'title', 'aria-label', 'alt', 'value'];
      document.querySelectorAll('*').forEach(el => {
        attrs.forEach(attr => {
          if (!el.hasAttribute(attr)) return;
          const original = el.getAttribute(attr);
          const fixed = decodeMojibake(original);
          if (fixed !== original) el.setAttribute(attr, fixed);
        });
      });
    } finally {
      mojibakeRepairing = false;
    }
  }

  function scheduleMojibakeRepair() {
    if (mojibakeRepairQueued) return;
    mojibakeRepairQueued = true;
    const schedule = window.requestAnimationFrame || ((cb) => setTimeout(cb, 16));
    schedule(() => {
      mojibakeRepairQueued = false;
      repairMojibakeDom();
    });
  }

  function startMojibakeObserver() {
    if (mojibakeObserverStarted || !document.body || typeof MutationObserver === 'undefined') return;
    mojibakeObserverStarted = true;

    const observer = new MutationObserver(() => {
      if (mojibakeRepairing) return;
      scheduleMojibakeRepair();
    });

    observer.observe(document.body, {
      subtree: true,
      childList: true,
      characterData: true,
      attributes: true,
      attributeFilter: ['placeholder', 'title', 'aria-label', 'alt', 'value']
    });
  }

  function renderTranslatedText(el, translated, iconHtml = '') {
    translated = decodeMojibake(translated);
    if (/<br\s*\/?>/i.test(translated)) {
      const safeHtml = translated.split(/<br\s*\/?>/i).map(escapeHtml).join('<br>');
      el.innerHTML = iconHtml ? iconHtml + ' ' + safeHtml : safeHtml;
      return;
    }

    if (iconHtml) {
      el.innerHTML = iconHtml + ' ' + escapeHtml(translated);
    } else {
      el.textContent = translated;
    }
  }

  function translatePage() {
    const lang = getCurrentLang();
    const t = TRANSLATIONS[lang] || TRANSLATIONS.th;

    // 1. textContent Translation
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key]) {
        const translated = interpolate(t[key], getI18nParams(el));
        const icon = el.querySelector('.material-icons, .material-icons-outlined');
        if (icon) {
          const iconHtml = icon.outerHTML;
          renderTranslatedText(el, translated, iconHtml);
        } else {
          renderTranslatedText(el, translated);
        }
      }
    });

    // 2. Attribute-based Translations
    const attrMap = {
      'data-i18n-placeholder': 'placeholder',
      'data-i18n-title': 'title',
      'data-i18n-aria-label': 'aria-label',
      'data-i18n-alt': 'alt',
      'data-i18n-value': 'value'
    };

    Object.keys(attrMap).forEach(dataAttr => {
      const targetAttr = attrMap[dataAttr];
      document.querySelectorAll(`[${dataAttr}]`).forEach(el => {
        const key = el.getAttribute(dataAttr);
        if (t[key]) el.setAttribute(targetAttr, decodeMojibake(interpolate(t[key], getI18nParams(el))));
      });
    });

    // 3. Document Title
    const titleKey = document.querySelector('title')?.getAttribute('data-i18n');
    if (titleKey && t[titleKey]) {
      const translatedTitle = decodeMojibake(t[titleKey]);
      document.title = /agriprice/i.test(translatedTitle)
        ? translatedTitle
        : translatedTitle + ' | AGRIPRICE';
    }

    repairMojibakeDom();

    window.dispatchEvent(new CustomEvent('i18n:updated', { detail: { lang } }));
  }

  function i18nT(key, fallback, params) {
    if (fallback && typeof fallback === 'object') {
      params = fallback;
      fallback = undefined;
    }

    const lang = getCurrentLang();
    const t = TRANSLATIONS[lang] || TRANSLATIONS.th;
    if (Object.prototype.hasOwnProperty.call(t, key)) return decodeMojibake(interpolate(t[key], params));
    if (lang !== 'th' && TRANSLATIONS.th && Object.prototype.hasOwnProperty.call(TRANSLATIONS.th, key)) {
      return decodeMojibake(interpolate(TRANSLATIONS.th[key], params));
    }
    return decodeMojibake(interpolate(fallback == null ? key : fallback, params));
  }

  function i18nApiMessage(message, fallbackKey = 'error_occurred', fallback) {
    const value = String(message || '').trim();
    if (!value || getCurrentLang() === 'th' || !/[ก-๙]/.test(value)) return value;
    return i18nT(fallbackKey, fallback || 'An error occurred');
  }

  function setupLanguageSelector() {
    const selector = document.getElementById('langSelector');
    if (!selector) return;
    
    selector.innerHTML = '';
    const current = getCurrentLang();
    
    LANGUAGES.forEach(lang => {
      const btn = document.createElement('button');
      btn.type = 'button';
      btn.className = 'lang-btn' + (current === lang ? ' active' : '');
      btn.innerHTML = `
        <span class="lang-label">${decodeMojibake(LANGUAGE_LABELS[lang])}</span>
        ${current === lang ? '<span class="material-icons-outlined">check_circle</span>' : ''}
      `;
      btn.onclick = () => {
        if (current === lang) return;
        setCurrentLang(lang);
        translatePage();
        setupLanguageSelector();
        setTimeout(() => { if (window.location.reload) window.location.reload(); }, 300);
      };
      selector.appendChild(btn);
    });
  }

  window.i18nInit = function() {
    translatePage();
    setupLanguageSelector();
    repairMojibakeDom();
  };

  window.i18nT = i18nT;
  window.i18nApiMessage = i18nApiMessage;
  window.decodeMojibakeText = decodeMojibake;
  window.getCurrentLang = getCurrentLang;
  window.setCurrentLang = setCurrentLang;
  window.LANGUAGE_LABELS = LANGUAGE_LABELS;

  window.addEventListener('storage', function(e) {
    if (e.key === 'lang') window.i18nInit();
  });

  window.addEventListener('agriprice:preferences-ready', window.i18nInit);

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', window.i18nInit);
  } else {
    window.i18nInit();
  }
